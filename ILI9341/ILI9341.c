void ILI9341_Blacklight_Init(void) {
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOB, ENABLE);

    GPIO_InitTypeDef gpioStructure;
    gpioStructure.GPIO_Pin = GPIO_Pin_1;
    gpioStructure.GPIO_Mode = GPIO_Mode_AF;
    gpioStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(GPIOB, &gpioStructure);
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM3, ENABLE);

    TIM_TimeBaseInitTypeDef timerInitStructure;
    timerInitStructure.TIM_Prescaler = 999;
    timerInitStructure.TIM_CounterMode = TIM_CounterMode_Up;
    timerInitStructure.TIM_Period = 500;
    timerInitStructure.TIM_ClockDivision = TIM_CKD_DIV1;
    timerInitStructure.TIM_RepetitionCounter = 0;
    TIM_TimeBaseInit(TIM3, &timerInitStructure);

    TIM_Cmd(TIM3, ENABLE);
}

void ILI9341_SetBlacklight(uint8_t dutycycle)
{
		TIM_OCInitTypeDef outputChannelInit;
		uint16_t pulseValue;

		pulseValue=((100-dutycycle)*500)/100;
    outputChannelInit.TIM_OCMode = TIM_OCMode_PWM1;
    outputChannelInit.TIM_Pulse = pulseValue;
    outputChannelInit.TIM_OutputState = TIM_OutputState_Enable;
    outputChannelInit.TIM_OCPolarity = TIM_OCPolarity_High;

    TIM_OC4Init(TIM3, &outputChannelInit);
    TIM_OC4PreloadConfig(TIM3, TIM_OCPreload_Enable);

    GPIO_PinAFConfig(GPIOB, GPIO_PinSource1, GPIO_AF_TIM3);
}

void ILI9341_DrawImage(uint16_t x, uint16_t y, tImage img) {
		uint32_t pixels_count;

		ILI9341_SetCursorPosition(x, y, img.width+x-1, img.height+y-1);
		ILI9341_SendCommand(ILI9341_GRAM);

		pixels_count=img.width*img.height;

		/* Send everything */
		ILI9341_CS_RESET;
		ILI9341_RS_SET;

		/* Go to 16-bit SPI mode */
		SPI_SetDataSize(ILI9341_SPI, SPI_DataSize_16b);

		SPI_DMA_TransmitHalfWord(img.data, NULL, pixels_count);

		/* Wait till done */
		while (SPI_DMA_Working(ILI9341_SPI));

		ILI9341_CS_SET;

		/* Go back to 8-bit SPI mode */
		SPI_SetDataSize(ILI9341_SPI, SPI_DataSize_8b);
}

